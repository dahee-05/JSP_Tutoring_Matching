package dao;

public class Teacher {
	private int tchno;
	private String tch_id;
	private String password;
	private String resume;
	private int gender;
	private String phone;
	private String birth;
	private int tch_status;
	private String name;
	public int getTchno() {
		return tchno;
	}
	public void setTchno(int tchno) {
		this.tchno = tchno;
	}
	public String getTch_id() {
		return tch_id;
	}
	public void setTch_id(String tch_id) {
		this.tch_id = tch_id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getResume() {
		return resume;
	}
	public void setResume(String resume) {
		this.resume = resume;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public int getTch_status() {
		return tch_status;
	}
	public void setTch_status(int tch_status) {
		this.tch_status = tch_status;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
